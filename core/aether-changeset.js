/**
 * AETHER Change Sets — PR-style grouped review of Ghost commits
 * Title · body · files · stats · Accept/Reject set · export markdown
 */
(function (g) {
  'use strict';

  var CS_KEY = 'aether_changesets_v1';
  var _activeId = null;

  function loadAll() {
    try {
      return JSON.parse(localStorage.getItem(CS_KEY) || '[]');
    } catch (e) {
      return [];
    }
  }

  function saveAll(list) {
    try {
      localStorage.setItem(CS_KEY, JSON.stringify(list.slice(0, 30)));
    } catch (e) {}
  }

  function uid() {
    return 'cs_' + Date.now().toString(36);
  }

  function esc(s) {
    return String(s || '')
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;');
  }

  function pendingGhosts() {
    if (!g.AETHER_Ghost || !g.AETHER_Ghost.loadQueue) return [];
    return g.AETHER_Ghost.loadQueue().filter(function (x) {
      return x.status === 'pending';
    });
  }

  /**
   * Create a PR-style change set from current pending ghosts.
   */
  function createFromPending(opts) {
    opts = opts || {};
    var pend = pendingGhosts();
    if (!pend.length) {
      if (g.showNotification) g.showNotification('No pending ghosts to group', 'warn');
      return null;
    }
    var adds = 0,
      dels = 0;
    var files = pend.map(function (p) {
      adds += (p.stats && p.stats.adds) || 0;
      dels += (p.stats && p.stats.dels) || 0;
      return {
        path: p.path,
        ghostId: p.id,
        kind: p.kind,
        adds: (p.stats && p.stats.adds) || 0,
        dels: (p.stats && p.stats.dels) || 0,
        message: p.message || '',
      };
    });
    var title =
      opts.title ||
      'AETHER patch: ' +
        files
          .slice(0, 3)
          .map(function (f) {
            return f.path.split('/').pop();
          })
          .join(', ') +
        (files.length > 3 ? ' +' + (files.length - 3) : '');
    var body =
      opts.body ||
      autoSummary(files, adds, dels);

    var cs = {
      id: uid(),
      t: Date.now(),
      title: title,
      body: body,
      files: files,
      stats: { adds: adds, dels: dels, files: files.length },
      status: 'open',
      ghostIds: pend.map(function (p) {
        return p.id;
      }),
    };
    var list = loadAll();
    list.unshift(cs);
    saveAll(list);
    _activeId = cs.id;
    renderPanel(cs);
    if (g.showNotification) {
      g.showNotification('Change set: ' + files.length + ' file(s) · +' + adds + '/-' + dels, 'success');
    }
    return cs;
  }

  function autoSummary(files, adds, dels) {
    var lines = [
      '## Summary',
      'Agent-proposed change set from AETHER Ghost commits.',
      '',
      '**Stats:** ' + files.length + ' files · +' + adds + ' / -' + dels,
      '',
      '## Files',
    ];
    files.forEach(function (f) {
      lines.push('- `' + f.path + '` (+' + f.adds + '/-' + f.dels + ') — ' + (f.message || f.kind));
    });
    lines.push('', '## Test plan', '- [ ] `/verify` or project test suite', '- [ ] Manual smoke of touched paths');
    return lines.join('\n');
  }

  function getActive() {
    if (!_activeId) {
      var list = loadAll();
      return list.find(function (c) {
        return c.status === 'open';
      }) || null;
    }
    return (
      loadAll().find(function (c) {
        return c.id === _activeId;
      }) || null
    );
  }

  function toMarkdown(cs) {
    cs = cs || getActive();
    if (!cs) return '';
    var md = '# ' + cs.title + '\n\n';
    md += cs.body + '\n\n';
    md += '---\n_Generated by AETHER Code · change set `' + cs.id + '`_\n';
    return md;
  }

  function exportMarkdown(cs) {
    var md = toMarkdown(cs);
    if (!md) {
      if (g.showNotification) g.showNotification('No change set', 'warn');
      return;
    }
    try {
      var blob = new Blob([md], { type: 'text/markdown' });
      var a = document.createElement('a');
      a.href = URL.createObjectURL(blob);
      a.download = 'aether-pr-' + (cs || getActive()).id + '.md';
      a.click();
      if (g.showNotification) g.showNotification('PR markdown downloaded', 'success');
    } catch (e) {
      if (typeof g.addSystemMessage === 'function') g.addSystemMessage('```markdown\n' + md + '\n```');
    }
  }

  function copyMarkdown(cs) {
    var md = toMarkdown(cs);
    if (!md) return;
    if (navigator.clipboard && navigator.clipboard.writeText) {
      navigator.clipboard.writeText(md).then(function () {
        if (g.showNotification) g.showNotification('PR body copied', 'success');
      });
    } else if (typeof g.addSystemMessage === 'function') {
      g.addSystemMessage('```markdown\n' + md + '\n```');
    }
  }

  async function acceptSet(id) {
    var list = loadAll();
    var cs = list.find(function (c) {
      return c.id === (id || _activeId);
    });
    if (!cs) {
      if (g.showNotification) g.showNotification('No open change set', 'warn');
      return;
    }
    // Checkpoint first
    if (g.AETHER_CodePro && g.AETHER_CodePro.snapshotPaths) {
      await g.AETHER_CodePro.snapshotPaths(
        cs.files.map(function (f) {
          return f.path;
        }),
        'pre-pr-accept'
      );
    }
    var n = 0;
    for (var i = 0; i < cs.ghostIds.length; i++) {
      try {
        if (g.AETHER_Ghost && g.AETHER_Ghost.accept) {
          var r = await g.AETHER_Ghost.accept(cs.ghostIds[i]);
          if (r && r.ok) n++;
        }
      } catch (e) {}
    }
    cs.status = 'merged';
    cs.mergedAt = Date.now();
    saveAll(list);
    renderPanel(null);
    if (g.showNotification) g.showNotification('Merged change set (' + n + ' files)', 'success');
    if (g.AETHER_CodePro && g.AETHER_CodePro.runVerify && localStorage.getItem('aether_code_auto_verify') === '1') {
      g.AETHER_CodePro.runVerify();
    }
  }

  function rejectSet(id) {
    var list = loadAll();
    var cs = list.find(function (c) {
      return c.id === (id || _activeId);
    });
    if (!cs) return;
    (cs.ghostIds || []).forEach(function (gid) {
      try {
        if (g.AETHER_Ghost) g.AETHER_Ghost.reject(gid);
      } catch (e) {}
    });
    cs.status = 'closed';
    cs.closedAt = Date.now();
    saveAll(list);
    renderPanel(null);
    if (g.showNotification) g.showNotification('Change set rejected', 'warn');
  }

  function renderPanel(cs) {
    var host = document.getElementById('code-changeset-panel');
    if (!host) {
      host = document.createElement('div');
      host.id = 'code-changeset-panel';
      host.className = 'code-changeset-panel';
      var bar = document.getElementById('code-pro-bar');
      if (bar && bar.parentNode) bar.parentNode.insertBefore(host, bar.nextSibling);
      else {
        var rail = document.getElementById('code-session-rail');
        if (rail && rail.parentNode) rail.parentNode.insertBefore(host, rail.nextSibling);
        else document.body.appendChild(host);
      }
    }
    var on =
      (g.state && g.state.codingMode) ||
      (document.body && document.body.classList.contains('coding-mode'));
    if (!on) {
      host.style.display = 'none';
      return;
    }
    cs = cs || getActive();
    if (!cs || cs.status !== 'open') {
      host.style.display = 'none';
      host.innerHTML = '';
      return;
    }
    host.style.display = 'block';
    host.innerHTML =
      '<div class="cs-hdr">' +
      '<span class="cs-badge">PR</span>' +
      '<span class="cs-title">' +
      esc(cs.title) +
      '</span>' +
      '<span class="cs-stats">+' +
      cs.stats.adds +
      '/-' +
      cs.stats.dels +
      ' · ' +
      cs.stats.files +
      ' files</span>' +
      '</div>' +
      '<div class="cs-files">' +
      cs.files
        .map(function (f) {
          return (
            '<div class="cs-file"><code>' +
            esc(f.path) +
            '</code> <span>+' +
            f.adds +
            '/-' +
            f.dels +
            '</span></div>'
          );
        })
        .join('') +
      '</div>' +
      '<div class="cs-actions">' +
      '<button type="button" class="code-rail-btn" id="cs-merge">Merge (Accept all)</button>' +
      '<button type="button" class="code-rail-btn" id="cs-close">Close</button>' +
      '<button type="button" class="code-rail-btn" id="cs-copy">Copy PR</button>' +
      '<button type="button" class="code-rail-btn" id="cs-export">Export .md</button>' +
      '<button type="button" class="code-rail-btn" id="cs-review">AI Review</button>' +
      '</div>';

    var b1 = document.getElementById('cs-merge');
    if (b1) b1.onclick = function () {
      acceptSet(cs.id);
    };
    var b2 = document.getElementById('cs-close');
    if (b2) b2.onclick = function () {
      rejectSet(cs.id);
    };
    var b3 = document.getElementById('cs-copy');
    if (b3) b3.onclick = function () {
      copyMarkdown(cs);
    };
    var b4 = document.getElementById('cs-export');
    if (b4) b4.onclick = function () {
      exportMarkdown(cs);
    };
    var b5 = document.getElementById('cs-review');
    if (b5)
      b5.onclick = function () {
        if (g.AETHER_CodePro && g.AETHER_CodePro.reviewPrompt) g.AETHER_CodePro.reviewPrompt();
        else if (g.AETHER_Subagents) {
          g.AETHER_Subagents.spawn({
            role: 'review',
            goal: 'Review this change set:\n' + toMarkdown(cs),
          });
        }
      };
  }

  function refresh() {
    renderPanel(getActive());
  }

  g.AETHER_ChangeSet = {
    createFromPending: createFromPending,
    createFromGhosts: createFromPending, // alias for Moat / Ship fusion
    getActive: getActive,
    loadAll: loadAll,
    toMarkdown: toMarkdown,
    exportMarkdown: exportMarkdown,
    copyMarkdown: copyMarkdown,
    acceptSet: acceptSet,
    rejectSet: rejectSet,
    renderPanel: renderPanel,
    refresh: refresh,
  };
  // legacy alias
  g.AETHER_Changeset = g.AETHER_ChangeSet;

  // Keep panel in sync with coding mode
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function () {
      setTimeout(refresh, 600);
    });
  } else {
    setTimeout(refresh, 600);
  }
})(typeof globalThis !== 'undefined' ? globalThis : window);
